name = "Ageng Susila"
# len Function commonly used to count the number of characters in the string
print(len(name))
# .upper method used to converting a string to upper case
print(name.upper())
# .lower method used to converting a string to lower case
print(name.lower())
# .find() method is will return the index of characters in the string 
print(name.find('S'))
# .replace() method is will replace characters or sequences in the string
print(name.replace("Susila", "Cipta Alam"))
# last, called "IN" operator used for checking the word in the string, also in operator will return boolean value
print("Susila" in name)